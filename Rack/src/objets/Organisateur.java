package objets;

public class Organisateur {

    private String nom_orga;
    private String prenom_orga;
    private String civi_orga;
    private String adresse_orga;
    private int tel_orga;
    private int fax_orga;
    private String mail_orga;
    private String entreprise_orga;

    public Organisateur(){
    }

    private String getNom_orga() {
        return this.nom_orga;
    }

    private void setNom_orga(String value) {
        this.nom_orga = value;
    }

    private String getPrenom_orga() {
        return this.prenom_orga;
    }

    private void setPrenom_orga(String value) {
        this.prenom_orga = value;
    }

    private String getCivi_orga() {
        return this.civi_orga;
    }

    private void setCivi_orga(String value) {
        this.civi_orga = value;
    }

    private String getAdresse_orga() {
        return this.adresse_orga;
    }

    private void setAdresse_orga(String value) {
        this.adresse_orga = value;
    }

    private int getTel_orga() {
        return this.tel_orga;
    }

    private void setTel_orga(int value) {
        this.tel_orga = value;
    }

    private int getFax_orga() {
        return this.fax_orga;
    }

    private void setFax_orga(int value) {
        this.fax_orga = value;
    }

    private String getMail_orga() {
        return this.mail_orga;
    }

    private void setMail_orga(String value) {
        this.mail_orga = value;
    }

    private String getEntreprise_orga() {
        return this.entreprise_orga;
    }

    private void setEntreprise_orga(String value) {
        this.entreprise_orga = value;
    }
}
